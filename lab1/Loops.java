public class Loops {
    public static void main(String[] args) {
        for(int i = 1; i != 6; i++) {
            System.out.println(i);
        }

        for(int i = 1; i != 16; i++) {
            System.out.println(i);
        }

        for(int i = 15; i != 0; i--) {
            System.out.println(i);
        }

        for(int i = 1; i != 10; i++) {
            System.out.println(i*5);
        }
    }
}